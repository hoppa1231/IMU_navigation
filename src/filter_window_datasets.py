#!/usr/bin/env python3
"""Create filtered copies of window datasets for controlled experiments."""

from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path

import numpy as np


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("--datasets", nargs="*", type=Path, default=None)
    parser.add_argument("--out-dir", type=Path, default=Path("derived/datasets"))
    parser.add_argument("--suffix", default="trim5")
    parser.add_argument("--min-time-s", type=float, default=5.0)
    parser.add_argument("--max-abs-dz-m", type=float, default=0.0, help="0 disables this filter.")
    parser.add_argument("--min-horizontal-target-m", type=float, default=0.0, help="0 disables this filter.")
    parser.add_argument("--report", type=Path, default=Path("reports/window_dataset_filters.md"))
    return parser.parse_args()


def read_meta(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as file:
        return list(csv.DictReader(file))


def write_meta(path: Path, rows: list[dict[str, str]]) -> None:
    if not rows:
        raise ValueError(f"No rows to write to {path}")
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as file:
        writer = csv.DictWriter(file, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)


def filter_dataset(
    path: Path,
    out_dir: Path,
    suffix: str,
    min_time_s: float,
    max_abs_dz_m: float,
    min_horizontal_target_m: float,
) -> dict[str, object]:
    data = np.load(path, allow_pickle=False)
    y = data["y"]
    time_s = data["time_s"]
    keep = time_s >= min_time_s
    if max_abs_dz_m > 0.0:
        keep &= np.abs(y[:, 2]) <= max_abs_dz_m
    if min_horizontal_target_m > 0.0:
        keep &= np.linalg.norm(y[:, :2], axis=1) >= min_horizontal_target_m

    out_npz = out_dir / f"{path.stem}_{suffix}.npz"
    out_npz.parent.mkdir(parents=True, exist_ok=True)
    payload = {name: data[name][keep] if data[name].shape[:1] == keep.shape else data[name] for name in data.files}
    np.savez_compressed(out_npz, **payload)

    features_src = path.with_name(f"{path.stem}_features.json")
    features_dst = out_dir / f"{path.stem}_{suffix}_features.json"
    if features_src.exists():
        feature_payload = json.loads(features_src.read_text(encoding="utf-8"))
        feature_payload["filter"] = {
            "source_dataset": str(path),
            "min_time_s": min_time_s,
            "max_abs_dz_m": max_abs_dz_m if max_abs_dz_m > 0.0 else None,
            "min_horizontal_target_m": min_horizontal_target_m if min_horizontal_target_m > 0.0 else None,
        }
        features_dst.write_text(json.dumps(feature_payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

    meta_src = path.with_name(f"{path.stem}_meta.csv")
    meta_dst = out_dir / f"{path.stem}_{suffix}_meta.csv"
    if meta_src.exists():
        meta_rows = read_meta(meta_src)
        filtered_meta = [row for row, keep_row in zip(meta_rows, keep.tolist()) if keep_row]
        write_meta(meta_dst, filtered_meta)

    return {
        "source": path,
        "output": out_npz,
        "source_windows": int(len(keep)),
        "kept_windows": int(np.sum(keep)),
        "removed_windows": int(len(keep) - np.sum(keep)),
        "x_shape": tuple(payload["x"].shape),
        "y_shape": tuple(payload["y"].shape),
        "features": features_dst,
        "meta": meta_dst,
    }


def write_report(
    path: Path,
    rows: list[dict[str, object]],
    min_time_s: float,
    max_abs_dz_m: float,
    min_horizontal_target_m: float,
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Window Dataset Filters",
        "",
        "This report is generated by `src/filter_window_datasets.py`.",
        "",
        f"- min time: `{min_time_s}` s",
        f"- max abs dz: `{max_abs_dz_m if max_abs_dz_m > 0.0 else 'disabled'}`",
        f"- min horizontal target: `{min_horizontal_target_m if min_horizontal_target_m > 0.0 else 'disabled'}`",
        "",
        "| source | output | source windows | kept | removed | X shape | y shape |",
        "| --- | --- | ---: | ---: | ---: | --- | --- |",
    ]
    for row in rows:
        lines.append(
            f"| `{row['source']}` | `{row['output']}` | {row['source_windows']} | {row['kept_windows']} | "
            f"{row['removed_windows']} | {row['x_shape']} | {row['y_shape']} |"
        )
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    args = parse_args()
    datasets = args.datasets
    if datasets is None:
        datasets = sorted(
            path
            for path in Path("derived/datasets").glob("windows_module_h*_l*.npz")
            if "_trim" not in path.stem and "_pathrel" not in path.stem and "_move" not in path.stem
        )
    rows = [
        filter_dataset(
            path,
            args.out_dir,
            args.suffix,
            args.min_time_s,
            args.max_abs_dz_m,
            args.min_horizontal_target_m,
        )
        for path in datasets
    ]
    write_report(args.report, rows, args.min_time_s, args.max_abs_dz_m, args.min_horizontal_target_m)
    print(f"Wrote {args.report}")
    for row in rows:
        print(f"Wrote {row['output']} {row['x_shape']} -> {row['y_shape']}")


if __name__ == "__main__":
    main()
