def write_html(path: Path, by_fold: dict[str, list[dict[str, float | str]]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(html_template(by_fold), encoding="utf-8")


def main() -> None:
    args = parse_args()
    source_rows = read_rows(args.pred_csv)
    grouped: dict[str, list[dict[str, str]]] = defaultdict(list)
    for row in source_rows:
        grouped[row.get("fold_id", "1")].append(row)

    by_fold = {fold_id: build_rollout(rows) for fold_id, rows in grouped.items()}
    all_rollout_rows = [row for rows in by_fold.values() for row in rows]
    write_rollout_csv(args.rollout_csv, all_rollout_rows)
    write_report(args.report, args.pred_csv, args.rollout_csv, args.html, by_fold)
    write_html(args.html, by_fold)
    print(f"Wrote {args.report}")
    print(f"Wrote {args.rollout_csv}")
    print(f"Wrote {args.html}")


if __name__ == "__main__":
    main()
