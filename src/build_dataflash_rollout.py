#!/usr/bin/env python3
"""Build sparse non-overlapping rollout diagnostics from DataFlash predictions."""

from __future__ import annotations

import argparse
import csv
import json
import math
from collections import defaultdict
from pathlib import Path


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--pred-csv",
        type=Path,
        default=Path("derived/predictions/dataflash_rolling_validation/imu_att_h5000_l5000/ridge_pred.csv"),
    )
    parser.add_argument("--report", type=Path, default=Path("reports/experiments/dataflash_rollout_imu_att_h5000.md"))
    parser.add_argument("--rollout-csv", type=Path, default=Path("derived/predictions/dataflash_rollout/imu_att_h5000_l5000_rollout.csv"))
    parser.add_argument("--html", type=Path, default=Path("artifacts/generated/dataflash_rollout/index.html"))
    return parser.parse_args()


def as_float(value: str) -> float:
    try:
        return float(value)
    except ValueError:
        return math.nan


def read_rows(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as file:
        return list(csv.DictReader(file))


def select_non_overlapping(rows: list[dict[str, str]]) -> list[dict[str, str]]:
    selected: list[dict[str, str]] = []
    next_time = -math.inf
    for row in sorted(rows, key=lambda item: as_float(item["time_s"])):
        time_s = as_float(row["time_s"])
        if time_s + 1e-9 < next_time:
            continue
        selected.append(row)
        next_time = as_float(row["future_time_s"])
    return selected


def build_rollout(rows: list[dict[str, str]]) -> list[dict[str, float | str]]:
    if not rows:
        return []
    selected = select_non_overlapping(rows)
    start = selected[0]
    pred_e = as_float(start["current_east_m"])
    pred_n = as_float(start["current_north_m"])
    pred_u = as_float(start["current_up_m"])
    result: list[dict[str, float | str]] = []
    for step_idx, row in enumerate(selected, start=1):
        pred_e += as_float(row["pred_dx_east_m"])
        pred_n += as_float(row["pred_dy_north_m"])
        pred_u += as_float(row["pred_dz_up_m"])
        true_e = as_float(row["true_future_east_m"])
        true_n = as_float(row["true_future_north_m"])
        true_u = as_float(row["true_future_up_m"])
        err_e = pred_e - true_e
        err_n = pred_n - true_n
        err_u = pred_u - true_u
        err_3d = math.sqrt(err_e * err_e + err_n * err_n + err_u * err_u)
        result.append(
            {
                "fold_id": row.get("fold_id", "1"),
                "step": step_idx,
                "time_s": as_float(row["future_time_s"]),
                "true_east_m": true_e,
                "true_north_m": true_n,
                "true_up_m": true_u,
                "pred_east_m": pred_e,
                "pred_north_m": pred_n,
                "pred_up_m": pred_u,
                "err_east_m": err_e,
                "err_north_m": err_n,
                "err_up_m": err_u,
                "err_3d_m": err_3d,
            }
        )
    return result


def metrics(rows: list[dict[str, float | str]]) -> dict[str, float]:
    errors = [float(row["err_3d_m"]) for row in rows]
    if not errors:
        return {"steps": 0.0, "final_error_m": math.nan, "mean_error_m": math.nan, "max_error_m": math.nan}
    return {
        "steps": float(len(errors)),
        "final_error_m": errors[-1],
        "mean_error_m": sum(errors) / len(errors),
        "max_error_m": max(errors),
    }


def write_rollout_csv(path: Path, rows: list[dict[str, float | str]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = [
        "fold_id",
        "step",
        "time_s",
        "true_east_m",
        "true_north_m",
        "true_up_m",
        "pred_east_m",
        "pred_north_m",
        "pred_up_m",
        "err_east_m",
        "err_north_m",
        "err_up_m",
        "err_3d_m",
    ]
    with path.open("w", newline="", encoding="utf-8") as file:
        writer = csv.DictWriter(file, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow({name: format_value(row[name]) for name in fieldnames})


def format_value(value: float | str) -> str:
    if isinstance(value, float):
        return f"{value:.6f}"
    return str(value)


def write_report(path: Path, pred_csv: Path, rollout_csv: Path, html: Path, by_fold: dict[str, list[dict[str, float | str]]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    all_rows = [row for rows in by_fold.values() for row in rows]
    lines = [
        "# DataFlash Sparse Rollout",
        "",
        "This report is generated by `src/build_dataflash_rollout.py`.",
        "",
        f"Source predictions: `{pred_csv}`",
        f"Rollout CSV: `{rollout_csv}`",
        f"HTML: `{html}`",
        "",
        "Method: within each rolling-validation fold, use non-overlapping prediction rows only. Start from the true current POS at the first selected row, then accumulate predicted 5-second displacement vectors.",
        "",
        "This is a sparse displacement rollout, not a full inertial navigation integration at IMU rate.",
        "",
        "## Metrics",
        "",
        "| fold | steps | final error m | mean error m | max error m |",
        "| --- | ---: | ---: | ---: | ---: |",
    ]
    for fold_id, rows in sorted(by_fold.items(), key=lambda item: int(item[0])):
        values = metrics(rows)
        lines.append(
            f"| {fold_id} | {values['steps']:.0f} | {values['final_error_m']:.3f} | "
            f"{values['mean_error_m']:.3f} | {values['max_error_m']:.3f} |"
        )
    values = metrics(all_rows)
    lines.append(
        f"| all | {values['steps']:.0f} | {values['final_error_m']:.3f} | "
        f"{values['mean_error_m']:.3f} | {values['max_error_m']:.3f} |"
    )
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


